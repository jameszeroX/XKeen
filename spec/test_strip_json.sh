#!/bin/sh
# Тесты вырезания комментариев из JSONC (нужен jq)
#
# Баг, ради которого написаны: прежняя регулярка требовала лишнюю '*' внутри
# блока, поэтому обычный /* текст */ не вырезался и jq падал. Вскрыт на живом
# KN-1012 при записи настройки балансировщика в xkeen.json.

# Модуль целиком не подключаем: на верхнем уровне он ходит curl'ом в RCI
# роутера. Берём ровно проверяемую функцию — так тест бьёт по реальному коду.
extract() {
    awk '/^strip_json_comments\(\) \{/,/^\}/' "$1"
}
eval "$(extract /repo/scripts/_xkeen/01_info/01_info_variable.sh)"

pass=0; fail=0
check() {
    if [ "$2" = "$3" ]; then
        printf 'OK   %s\n' "$1"; pass=$((pass+1))
    else
        printf 'FAIL %s\n       ожидалось [%s]\n       получено  [%s]\n' "$1" "$3" "$2"; fail=$((fail+1))
    fi
}

# Прогон через jq нормализует пробелы: сравниваем разобранный JSON, а не текст
norm() { printf '%s\n' "$1" | strip_json_comments | jq -c . 2>/dev/null; }

# --- блочные комментарии ---
check "обычный /* c */"             "$(norm '{ /* c */ "a": 1 }')"       '{"a":1}'
check "без пробелов /*c*/"          "$(norm '{ /*c*/ "a": 1 }')"         '{"a":1}'
check "звёздочка внутри"            "$(norm '{ /* a * b */ "a": 1 }')"   '{"a":1}'
check "двойные звёздочки"           "$(norm '{ /** c **/ "a": 1 }')"     '{"a":1}'
check "слэш внутри комментария"     "$(norm '{ /* a/b */ "a": 1 }')"     '{"a":1}'
check "два блока подряд"            "$(norm '{ /*a*//*b*/ "a": 1 }')"    '{"a":1}'

check "многострочный блок" "$(norm '{
  /* строка один
     строка два */
  "a": 1
}')" '{"a":1}'

# --- строчные комментарии ---
check "// в начале строки" "$(norm '{
  // комментарий
  "a": 1
}')" '{"a":1}'

check "// хвостовой" "$(norm '{ "a": 1 // хвост
}')" '{"a":1}'

# --- строковые значения не должны страдать ---
# Регулярка этого не умела: она не отличает комментарий от строки
check "URL в строке цел"            "$(norm '{ "u": "https://x", "a": 1 }')"   '{"u":"https://x","a":1}'
check "// внутри строки цел"        "$(norm '{ "n": "a // b" }')"              '{"n":"a // b"}'
check "/* внутри строки цел"        "$(norm '{ "u": "x/*y" }')"                '{"u":"x/*y"}'
check "экранированная кавычка"      "$(norm '{ "q": "a\"b // c" }')"           '{"q":"a\"b // c"}'

# Самый злой случай: незакрытый /* в строке и настоящий комментарий ниже.
# Регулярка склеивала их и вырезала кусок конфига между ними.
check "/* в строке + блок ниже" "$(norm '{
  "u": "x/*y",
  /* реальный коммент */
  "a": 1
}')" '{"u":"x/*y","a":1}'

# --- отсутствие комментариев ничего не портит ---
check "чистый JSON не меняется"     "$(norm '{ "a": 1, "b": [1,2] }')"   '{"a":1,"b":[1,2]}'
check "пустой объект"               "$(norm '{}')"                       '{}'

# --- обе копии функции обязаны совпадать ---
# 04_register_init.sh побайтово копируется в init.d/S05xkeen и модули не
# подключает, поэтому функция там продублирована. Копии обязаны не разъезжаться.
extract() {
    awk '/^strip_json_comments\(\) \{/,/^\}/' "$1"
}
a=$(extract /repo/scripts/_xkeen/01_info/01_info_variable.sh)
b=$(extract /repo/scripts/_xkeen/02_install/07_install_register/04_register_init.sh)
check "копии функции идентичны" "$([ "$a" = "$b" ] && echo да || echo нет)" "да"
check "функция вообще найдена" "$([ -n "$a" ] && echo да || echo нет)" "да"

printf '\n=== пройдено: %s, провалено: %s ===\n' "$pass" "$fail"
[ "$fail" -eq 0 ]
